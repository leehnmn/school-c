#include <stdio.h>

int main() 
{
	int a;
	a = 127;

	printf("a=%3o in %d-number system \n", a, 8);
	printf("a=%3d in %d-number system \n", a, 10);
	printf("a=%3x in %d-number system \n", a, 16);
	return 0;
}




